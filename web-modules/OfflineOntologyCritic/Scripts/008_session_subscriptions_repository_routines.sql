CREATE OR REPLACE FUNCTION session_subscription_search_match(p_subscription session_subscriptions,p_search text) RETURNS boolean LANGUAGE sql IMMUTABLE AS $$
	SELECT p_subscription.session_subscription_id::text ILIKE '%'||COALESCE(p_search,'')||'%'
		OR p_subscription.subscription_identity ILIKE '%'||COALESCE(p_search,'')||'%'
		OR p_subscription.subscriber_session_key ILIKE '%'||COALESCE(p_search,'')||'%'
		OR p_subscription.event_type ILIKE '%'||COALESCE(p_search,'')||'%'
		OR p_subscription.delivery_mode ILIKE '%'||COALESCE(p_search,'')||'%'
		OR COALESCE(p_subscription.callback_url,'') ILIKE '%'||COALESCE(p_search,'')||'%';
$$;

CREATE OR REPLACE FUNCTION get_session_subscriptions_sp_count_sp(p_search text) RETURNS TABLE ("Total" integer) LANGUAGE sql AS $$
	SELECT COUNT(*)::integer FROM session_subscriptions s WHERE session_subscription_search_match(s, p_search);
$$;


CREATE OR REPLACE FUNCTION insert_session_subscription_sp(p_subscription_identity text,p_session_id integer,p_subscriber_session_key text,p_event_type text,p_delivery_mode text,p_callback_url text,p_is_enabled boolean,p_expiration_utc timestamp,p_data text)

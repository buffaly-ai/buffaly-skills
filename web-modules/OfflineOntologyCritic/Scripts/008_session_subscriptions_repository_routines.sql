CREATE TABLE IF NOT EXISTS session_subscriptions (
	session_subscription_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	subscription_identity text NOT NULL UNIQUE,
	session_id integer NOT NULL REFERENCES sessions(session_id) ON DELETE CASCADE,
	subscriber_session_key text NOT NULL,
	event_type text NOT NULL,
	delivery_mode text NOT NULL,
	callback_url text NULL,
	is_enabled boolean NOT NULL DEFAULT true,
	expiration_utc timestamp NULL,
	date_created timestamp NOT NULL DEFAULT now(),
	last_updated timestamp NOT NULL DEFAULT now(),
	data text NULL
);
CREATE INDEX IF NOT EXISTS ix_session_subscriptions_session_id ON session_subscriptions(session_id);
CREATE INDEX IF NOT EXISTS ix_session_subscriptions_subscriber_session_key ON session_subscriptions(subscriber_session_key);
CREATE INDEX IF NOT EXISTS ix_session_subscriptions_event_type ON session_subscriptions(event_type);


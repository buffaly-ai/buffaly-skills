CREATE TABLE IF NOT EXISTS page_layouts (
	page_layout_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	url varchar(255) NOT NULL,
	handler varchar(255) NOT NULL,
	is_enabled boolean NOT NULL DEFAULT true,
	date_created timestamp NOT NULL DEFAULT now(),
	last_updated timestamp NOT NULL DEFAULT now(),
	page_title varchar(255) NULL,
	site_id integer NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS ix_page_layouts_url ON page_layouts (url);

CREATE OR REPLACE FUNCTION insert_page_layout_sp(p_url varchar(255), p_handler varchar(255), p_is_enabled boolean, p_page_title varchar(255), p_site_id integer)
RETURNS TABLE ("PageLayoutID" integer) LANGUAGE sql AS $$
	INSERT INTO page_layouts (url, handler, is_enabled, date_created, last_updated, page_title, site_id)
	VALUES (p_url, p_handler, p_is_enabled, now(), now(), p_page_title, p_site_id)
	RETURNING page_layout_id;
$$;

CREATE OR REPLACE FUNCTION update_page_layout_sp(p_page_layout_id integer, p_url varchar(255), p_handler varchar(255), p_is_enabled boolean, p_page_title varchar(255), p_site_id integer)
RETURNS void LANGUAGE sql AS $$
	UPDATE page_layouts SET url=p_url, handler=p_handler, is_enabled=p_is_enabled, last_updated=now(), page_title=p_page_title, site_id=p_site_id
	WHERE page_layout_id=p_page_layout_id;
$$;

CREATE OR REPLACE FUNCTION remove_page_layout_sp(p_page_layout_id integer)
RETURNS void LANGUAGE sql AS $$ DELETE FROM page_layouts WHERE page_layout_id=p_page_layout_id; $$;

CREATE OR REPLACE FUNCTION copy_page_layout_sp(p_page_layout_id integer)
RETURNS TABLE ("PageLayoutID" integer) LANGUAGE sql AS $$
	INSERT INTO page_layouts (url, handler, is_enabled, date_created, last_updated, page_title, site_id)
	SELECT url || '-copy-' || floor(extract(epoch from clock_timestamp()) * 1000000)::bigint::text, handler, is_enabled, now(), now(), page_title, site_id
	FROM page_layouts WHERE page_layout_id=p_page_layout_id
	RETURNING page_layout_id;
$$;


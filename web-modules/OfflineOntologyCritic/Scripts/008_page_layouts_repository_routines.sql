RETURNS TABLE ("PageLayoutID" integer, "Url" varchar(255), "Handler" varchar(255), "IsEnabled" boolean, "DateCreated" timestamp, "LastUpdated" timestamp, "PageTitle" varchar(255), "SiteID" integer)

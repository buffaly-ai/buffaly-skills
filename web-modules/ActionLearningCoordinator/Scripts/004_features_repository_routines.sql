CREATE TABLE IF NOT EXISTS features (
	feature_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	feature_name varchar(255) NOT NULL,
	version varchar(255) NULL,
	is_enabled boolean NOT NULL DEFAULT true,
	settings_assembly varchar(255) NULL,
	settings_class varchar(255) NOT NULL,
	settings text NULL,
	date_created timestamp NOT NULL DEFAULT now(),
	last_updated timestamp NOT NULL DEFAULT now(),
	data text NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS ix_features_feature_name ON features (feature_name);


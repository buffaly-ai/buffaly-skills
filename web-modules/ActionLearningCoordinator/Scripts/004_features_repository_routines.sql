CREATE OR REPLACE FUNCTION get_features_sp_paging_sp(p_search varchar(255), p_sort_column varchar(255), p_sort_ascending boolean, p_skip_rows integer, p_num_rows integer)
RETURNS TABLE ("FeatureID" integer, "FeatureName" varchar(255), "Version" varchar(255), "IsEnabled" boolean, "SettingsAssembly" varchar(255), "SettingsClass" varchar(255), "Settings" text, "DateCreated" timestamp, "LastUpdated" timestamp, "Data" text)
LANGUAGE sql AS $$
	SELECT feature_id, feature_name, version, is_enabled, settings_assembly, settings_class, settings, date_created, last_updated, data
	FROM features
	WHERE feature_id::varchar(50) LIKE '%'||COALESCE(p_search,'')||'%' OR feature_name LIKE '%'||COALESCE(p_search,'')||'%' OR version LIKE '%'||COALESCE(p_search,'')||'%' OR settings_assembly LIKE '%'||COALESCE(p_search,'')||'%' OR settings_class LIKE '%'||COALESCE(p_search,'')||'%'
	ORDER BY
		CASE WHEN p_sort_column='FeatureID' AND p_sort_ascending THEN feature_id END ASC,
		CASE WHEN p_sort_column='FeatureID' AND NOT p_sort_ascending THEN feature_id END DESC,
		CASE WHEN p_sort_column='FeatureName' AND p_sort_ascending THEN feature_name END ASC,
		CASE WHEN p_sort_column='FeatureName' AND NOT p_sort_ascending THEN feature_name END DESC,
		CASE WHEN p_sort_column='Version' AND p_sort_ascending THEN version END ASC,
		CASE WHEN p_sort_column='Version' AND NOT p_sort_ascending THEN version END DESC,
		CASE WHEN p_sort_column='IsEnabled' AND p_sort_ascending THEN is_enabled END ASC,
		CASE WHEN p_sort_column='IsEnabled' AND NOT p_sort_ascending THEN is_enabled END DESC,
		CASE WHEN p_sort_column='SettingsAssembly' AND p_sort_ascending THEN settings_assembly END ASC,
		CASE WHEN p_sort_column='SettingsAssembly' AND NOT p_sort_ascending THEN settings_assembly END DESC,
		CASE WHEN p_sort_column='SettingsClass' AND p_sort_ascending THEN settings_class END ASC,
		CASE WHEN p_sort_column='SettingsClass' AND NOT p_sort_ascending THEN settings_class END DESC,
		CASE WHEN p_sort_column='Settings' AND p_sort_ascending THEN settings END ASC,
		CASE WHEN p_sort_column='Settings' AND NOT p_sort_ascending THEN settings END DESC,
		CASE WHEN p_sort_column='DateCreated' AND p_sort_ascending THEN date_created END ASC,
		CASE WHEN p_sort_column='DateCreated' AND NOT p_sort_ascending THEN date_created END DESC,
		CASE WHEN p_sort_column='LastUpdated' AND p_sort_ascending THEN last_updated END ASC,
		CASE WHEN p_sort_column='LastUpdated' AND NOT p_sort_ascending THEN last_updated END DESC,
		CASE WHEN p_sort_column='Data' AND p_sort_ascending THEN data END ASC,
		CASE WHEN p_sort_column='Data' AND NOT p_sort_ascending THEN data END DESC
	OFFSET p_skip_rows LIMIT p_num_rows;
$$;


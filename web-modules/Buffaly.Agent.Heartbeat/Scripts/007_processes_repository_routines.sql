CREATE OR REPLACE FUNCTION stored_procedure_has_parameter_sp(p_stored_procedure_name text,p_parameter_name text)
RETURNS TABLE (HasParameter integer) LANGUAGE sql AS $$
	SELECT CASE WHEN EXISTS (
		SELECT 1
		FROM information_schema.parameters
		WHERE specific_schema = current_schema()
			AND specific_name LIKE lower(p_stored_procedure_name) || '_%'
			AND parameter_name = lower(regexp_replace(trim(leading '@' FROM p_parameter_name), '([A-Z])', '_\1', 'g'))
	) THEN 1 ELSE 0 END::integer;
$$;

CREATE OR REPLACE FUNCTION "StoredProcedureHasParameterSp"(p_stored_procedure_name text,p_parameter_name text)
RETURNS TABLE (HasParameter integer) LANGUAGE sql AS $$
SELECT * FROM stored_procedure_has_parameter_sp(p_stored_procedure_name,p_parameter_name);
$$;

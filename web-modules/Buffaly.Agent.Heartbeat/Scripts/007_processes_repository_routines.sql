CREATE OR REPLACE FUNCTION get_processes_sp_count_sp(p_search text)
RETURNS TABLE ("Total" integer)
LANGUAGE sql AS $$
	SELECT COUNT(*)::integer
	FROM processes
	WHERE process_id::text LIKE '%'||COALESCE(p_search,'')||'%'
		OR process_name LIKE '%'||COALESCE(p_search,'')||'%'
		OR url LIKE '%'||COALESCE(p_search,'')||'%'
		OR action LIKE '%'||COALESCE(p_search,'')||'%';
$$;

CREATE OR REPLACE FUNCTION get_processes_sp_paging_sp(p_search text,p_sort_column text,p_sort_ascending boolean,p_skip_rows integer,p_num_rows integer)
RETURNS TABLE ("ProcessID" integer,"ProcessName" text,"Url" text,"Action" text,"RunData" text,"IsRunning" boolean,"RunStarted" timestamp,"RunEnded" timestamp,"RunEvery" numeric,"IsEnabled" boolean,"DateCreated" timestamp,"LastUpdated" timestamp,"MaximumRunTime" integer,"IsTimedOut" boolean,"RunEveryType" text,"Data" text)
LANGUAGE sql AS $$
	SELECT *
	FROM get_process_rows()
	WHERE "ProcessID"::text LIKE '%'||COALESCE(p_search,'')||'%'
		OR "ProcessName" LIKE '%'||COALESCE(p_search,'')||'%'
		OR "Url" LIKE '%'||COALESCE(p_search,'')||'%'
		OR "Action" LIKE '%'||COALESCE(p_search,'')||'%'
	ORDER BY
		CASE WHEN p_sort_column='ProcessID' AND p_sort_ascending THEN "ProcessID" END ASC,
		CASE WHEN p_sort_column='ProcessID' AND NOT p_sort_ascending THEN "ProcessID" END DESC,
		CASE WHEN p_sort_column='ProcessName' AND p_sort_ascending THEN "ProcessName" END ASC,
		CASE WHEN p_sort_column='ProcessName' AND NOT p_sort_ascending THEN "ProcessName" END DESC,
		CASE WHEN p_sort_column='Url' AND p_sort_ascending THEN "Url" END ASC,
		CASE WHEN p_sort_column='Url' AND NOT p_sort_ascending THEN "Url" END DESC,
		CASE WHEN p_sort_column='Action' AND p_sort_ascending THEN "Action" END ASC,
		CASE WHEN p_sort_column='Action' AND NOT p_sort_ascending THEN "Action" END DESC,
		CASE WHEN p_sort_column='RunData' AND p_sort_ascending THEN "RunData" END ASC,
		CASE WHEN p_sort_column='RunData' AND NOT p_sort_ascending THEN "RunData" END DESC,
		CASE WHEN p_sort_column='RunStarted' AND p_sort_ascending THEN "RunStarted" END ASC,
		CASE WHEN p_sort_column='RunStarted' AND NOT p_sort_ascending THEN "RunStarted" END DESC,
		CASE WHEN p_sort_column='RunEnded' AND p_sort_ascending THEN "RunEnded" END ASC,
		CASE WHEN p_sort_column='RunEnded' AND NOT p_sort_ascending THEN "RunEnded" END DESC,
		CASE WHEN p_sort_column='DateCreated' AND p_sort_ascending THEN "DateCreated" END ASC,
		CASE WHEN p_sort_column='DateCreated' AND NOT p_sort_ascending THEN "DateCreated" END DESC,
		CASE WHEN p_sort_column='LastUpdated' AND p_sort_ascending THEN "LastUpdated" END ASC,
		CASE WHEN p_sort_column='LastUpdated' AND NOT p_sort_ascending THEN "LastUpdated" END DESC,
		"ProcessID" ASC
	OFFSET p_skip_rows LIMIT p_num_rows;
$$;


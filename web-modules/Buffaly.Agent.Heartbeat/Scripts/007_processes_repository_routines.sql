CREATE TABLE IF NOT EXISTS processes (
	process_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	process_name text NOT NULL UNIQUE,
	url text NULL,
	action text NULL,
	run_data text NULL,
	is_running boolean NOT NULL DEFAULT false,
	run_started timestamp NULL,
	run_ended timestamp NULL,
	run_every numeric(18,4) NULL,
	is_enabled boolean NOT NULL DEFAULT true,
	date_created timestamp NOT NULL DEFAULT now(),
	last_updated timestamp NOT NULL DEFAULT now(),
	maximum_run_time integer NULL,
	is_timed_out boolean NOT NULL DEFAULT false,
	run_every_type text NULL,
	data text NULL
);
CREATE UNIQUE INDEX IF NOT EXISTS ix_processes_process_name ON processes(process_name);

CREATE OR REPLACE FUNCTION get_process_rows()
RETURNS TABLE ("ProcessID" integer,"ProcessName" text,"Url" text,"Action" text,"RunData" text,"IsRunning" boolean,"RunStarted" timestamp,"RunEnded" timestamp,"RunEvery" numeric,"IsEnabled" boolean,"DateCreated" timestamp,"LastUpdated" timestamp,"MaximumRunTime" integer,"IsTimedOut" boolean,"RunEveryType" text,"Data" text)
LANGUAGE sql AS $$
	SELECT process_id,process_name,url,action,run_data,is_running,run_started,run_ended,run_every,is_enabled,date_created,last_updated,maximum_run_time,is_timed_out,run_every_type,data
	FROM processes;
$$;

CREATE OR REPLACE FUNCTION insert_process_sp(p_process_name text,p_url text,p_action text,p_run_data text,p_is_running boolean,p_run_started timestamp with time zone,p_run_ended timestamp with time zone,p_run_every numeric,p_is_enabled boolean,p_maximum_run_time integer,p_is_timed_out boolean)
RETURNS TABLE ("ProcessID" integer)
LANGUAGE sql AS $$
	INSERT INTO processes(process_name,url,action,run_data,is_running,run_started,run_ended,run_every,is_enabled,date_created,last_updated,maximum_run_time,is_timed_out)
	VALUES(p_process_name,p_url,p_action,p_run_data,p_is_running,p_run_started,p_run_ended,p_run_every,p_is_enabled,now(),now(),p_maximum_run_time,p_is_timed_out)
	RETURNING process_id;
$$;

CREATE OR REPLACE FUNCTION update_process_sp(p_process_id integer,p_process_name text,p_url text,p_action text,p_run_data text,p_is_running boolean,p_run_started timestamp with time zone,p_run_ended timestamp with time zone,p_run_every numeric,p_is_enabled boolean,p_maximum_run_time integer,p_is_timed_out boolean,p_run_every_type text DEFAULT NULL,p_data text DEFAULT NULL)
RETURNS void
LANGUAGE sql AS $$
	UPDATE processes
	SET process_name=p_process_name,
		url=p_url,
		action=p_action,
		run_data=p_run_data,
		is_running=p_is_running,
		run_started=p_run_started,
		run_ended=p_run_ended,
		run_every=p_run_every,
		is_enabled=p_is_enabled,
		last_updated=now(),
		maximum_run_time=p_maximum_run_time,
		is_timed_out=p_is_timed_out,
		run_every_type=p_run_every_type,
		data=p_data
	WHERE process_id=p_process_id;
$$;

